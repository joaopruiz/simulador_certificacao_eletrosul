package simulador.mode.usuarios;

public class Usuariotb {
	private int idUsuariosTB;
	private String usuarios;
	private String senha;
	private int tipo;
	public Usuariotb(int idUsuariosTB, String usuarios, String senha, int tipo) {
		super();
		this.idUsuariosTB = idUsuariosTB;
		this.usuarios = usuarios;
		this.senha = senha;
		this.tipo = tipo;
	}
	public int getIdUsuariosTB() {
		return idUsuariosTB;
	}
	public void setIdUsuariosTB(int idUsuariosTB) {
		this.idUsuariosTB = idUsuariosTB;
	}
	public String getUsuarios() {
		return usuarios;
	}
	public void setUsuarios(String usuarios) {
		this.usuarios = usuarios;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	
	
}
