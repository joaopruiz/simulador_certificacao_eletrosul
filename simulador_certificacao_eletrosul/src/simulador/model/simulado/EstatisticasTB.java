package simulador.model.simulado;

public class EstatisticasTB {
	private int num_simulado;
	private int num_pergunta;
	private int acertou;
	private int assunto_pergunta;
	private String resposta_correta;
	private String resposta_assinalada;
	private int id_usuario;
	public EstatisticasTB(int numSimulado, int numPergunta, int acertou,
			int assuntoPergunta, String respostaCorreta,
			String respostaAssinalada, int idUsuario) {
		super();
		num_simulado = numSimulado;
		num_pergunta = numPergunta;
		this.acertou = acertou;
		assunto_pergunta = assuntoPergunta;
		resposta_correta = respostaCorreta;
		resposta_assinalada = respostaAssinalada;
		id_usuario = idUsuario;
	}
	public int getNum_simulado() {
		return num_simulado;
	}
	public void setNum_simulado(int numSimulado) {
		num_simulado = numSimulado;
	}
	public int getNum_pergunta() {
		return num_pergunta;
	}
	public void setNum_pergunta(int numPergunta) {
		num_pergunta = numPergunta;
	}
	public int getAcertou() {
		return acertou;
	}
	public void setAcertou(int acertou) {
		this.acertou = acertou;
	}
	public int getAssunto_pergunta() {
		return assunto_pergunta;
	}
	public void setAssunto_pergunta(int assuntoPergunta) {
		assunto_pergunta = assuntoPergunta;
	}
	public String getResposta_correta() {
		return resposta_correta;
	}
	public void setResposta_correta(String respostaCorreta) {
		resposta_correta = respostaCorreta;
	}
	public String getResposta_assinalada() {
		return resposta_assinalada;
	}
	public void setResposta_assinalada(String respostaAssinalada) {
		resposta_assinalada = respostaAssinalada;
	}
	public int getId_usuario() {
		return id_usuario;
	}
	public void setId_usuario(int idUsuario) {
		id_usuario = idUsuario;
	}
	
	
}
